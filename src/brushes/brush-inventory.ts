export type BrushPressureOpacityRange = readonly [number, number];

export type BrushGeometryFamily =
  | "ribbon"
  | "tube"
  | "emissive"
  | "particle"
  | "unsupported";

export type BrushMaterialFamily =
  | "standard"
  | "unlit"
  | "additive"
  | "particle"
  | "fallback";

export interface OpenBrushExportManifest {
  tiltBrushVersion?: string;
  tiltBrushBuildStamp?: string;
  brushes: Record<string, OpenBrushExportBrush>;
}

export interface OpenBrushExportBrush {
  guid: string;
  name: string;
  folderName: string;
  shaderVersion: string;
  vertexShader?: string;
  fragmentShader?: string;
  blendMode: number;
  enableCull: boolean;
  textures?: Record<string, string>;
  textureSizes?: Record<string, [number, number]>;
  floatParams?: Record<string, number>;
  vectorParams?: Record<string, [number, number, number, number]>;
  colorParams?: Record<string, [number, number, number, number]>;
}

export interface BrushShaderAssetInfo {
  /** "handcrafted" = authored GLSL from the Open Brush repo; "template" = generated by the official template pipeline. */
  glslSource: "handcrafted" | "template";
  /** True when the vertex stage is the shared VertDefault program, i.e. it only consumes position/normal/color/uv. */
  vertexIsDefault: boolean;
  vertexShaderFile: string;
  fragmentShaderFile: string;
  /** Manifest texture param name (e.g. "MainTex") → extracted file under public/openbrush/textures/. */
  textureFiles: Record<string, string>;
  textureImporters: Record<string, BrushTextureImporterSettings>;
}

export interface BrushTextureImporterSettings {
  sRGB: boolean;
  mipmaps: boolean;
  filter: "point" | "bilinear" | "trilinear";
  wrapU: "repeat" | "clamp" | "mirror" | "mirror-once";
  wrapV: "repeat" | "clamp" | "mirror" | "mirror-once";
  anisotropy: number;
  mipBias: number;
}

